def print_arch():
    click.echo(click.style(ARCH, fg="cyan"))


def print_summary(app_name: str, language: str, port: int,
                  db: str | None, cpu: str, memory: str,
                  cloud: bool, output_dir):
    click.echo(header("DevOpsify Summary"))
    rows = [
        ("App name",      app_name),
        ("Language",      language),
        ("Port",          str(port)),
        ("Database",      db or "none"),
        ("CPU",           cpu),
        ("Memory",        memory),
        ("Target",        "AWS EKS" if cloud else "minikube"),
        ("Output dir",    str(output_dir)),
    ]
    for label, value in rows:
        click.echo(info(f"  {label:<14}: {value}"))
    click.echo("")